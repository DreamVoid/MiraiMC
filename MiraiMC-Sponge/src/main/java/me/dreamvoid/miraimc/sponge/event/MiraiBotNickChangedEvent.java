package me.dreamvoid.miraimc.sponge.event;

import net.mamoe.mirai.event.events.BotNickChangedEvent;
import org.jetbrains.annotations.NotNull;
import org.spongepowered.api.event.cause.Cause;
import org.spongepowered.api.event.impl.AbstractEvent;

/**
 * Bot 昵称改变
 */
public class MiraiBotNickChangedEvent extends AbstractEvent {

    private final Cause cause;

    public MiraiBotNickChangedEvent(BotNickChangedEvent event, Cause cause) {
        this.event = event;
        this.cause = cause;
    }

    private final BotNickChangedEvent event;

    /**
     * 获取机器人账号
     * @return 机器人账号
     */
    public long getID() { return event.getBot().getId(); }

    /**
     * 获取机器人更换前的昵称
     * @return 机器人更换前的昵称
     */
    public String getOldNick() { return event.getFrom(); }

    /**
     * 获取机器人更换后的昵称
     * @return 机器人更换后的昵称
     */
    public String getNewNick() { return event.getTo(); }

    /**
     * 获取原始事件内容<br>
     * [!] 不推荐使用
     * @return 原始事件内容
     */
    public String eventToString() {
        return event.toString();
    }

    @Override
    public @NotNull Cause getCause() {
        return cause;
    }
}
